
// Façam uma função que receba um número e defina se ele é positivo negativo ou nulo

function pnn(numero=0){
    let resultado;
    if (numero > 0){
        resultado = 'Positivo'
    }else if(numero == 0){
        resultado = 'Nulo'
    }
    else {
        resultado = 'Negativo'
    }
    return resultado
}
